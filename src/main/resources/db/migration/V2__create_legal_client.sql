CREATE TABLE IF NOT EXISTS LEGAL_CLIENT (
ID BIGINT NOT NULL AUTO_INCREMENT,
NAME VARCHAR(255) NOT NULL,
CREDIT BOOLEAN NOT NULL,
DEPOSIT BOOLEAN NOT NULL,
DEPOSIT_PRICE BIGINT,
RKO BOOLEAN NOT NULL,
RKO_PRICE BIGINT,
INVESTMENT BOOLEAN NOT NULL,
FACTORING BOOLEAN NOT NULL,
INTERNATIONAL_OP BOOLEAN NOT NULL,
INTERNET_BANKING BOOLEAN NOT NULL,
DBO_PRICE BIGINT,
RISKS BIGINT NOT NULL,
CONSTRAINT LEGAL_CLIENT_PK PRIMARY KEY (ID)
);